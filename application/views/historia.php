			Na primeira sessão preparatória foram apresentados os vereadores eleitos nas eleições procedidas no dia 02 de dezembro de 1901, os Srs. Cel. Francisco Braz Pereira Gomes, Joaquim José de Faria e Souza, José Serôdio, Joaquim Carlos da Silveira, Antônio Pereira de Mendonça, José Martins Tosta Junior, João Gonçalves Cintra, Joaquim Carlos da Silveira, Alfredo Vianna e Cândido Rennó. O Primeiro Presidente da Câmara e Agente do Executivo eleito (vez que não havia ainda a separação de poderes (Legislativo, Executivo, e Judiciário), como hoje, em que os poderes são distintos, independentes e harmônicos) foi o Cel. Francisco Braz Pereira Gomes, na segunda reunião preparatória da Câmara, por ele presidida interinamente. O Vice-Presidente e o Secretário foram eleitos na sessão de instalação da Câmara: Secretário José Serôdio e Vice-Presidente o Vereador Antonio Pereira de Mendonça. "Em seguida levantou-se o Presidente o qual proferiu uma breve alocução e ao terminar esta, invocando o precedente da promulgação do código fundamental de Minas Gerais, declarou alta vós, Em nome de Deus Todo Poderoso, instalado o Município de São Caetano da Vargem Grande". Em seguida o Vereador Antonio Pereira de Mendonça, Secretário, apresentou indicação, para congratulando com governo deste Estado, pela elevação da Vila a Município, pedindo que a moção fosse também assinada pelo povo, aprovada por unanimidade, sendo a ata assinada pelos vereadores e por todos os presentes.

			Nos anais da Câmara existem projetos de lei para a instalação da Câmara, inclusive junto a Cadeia Pública, na primeira década de 1900, entretanto, as Sessões, durante muitos anos se realizaram em sala própria no atual prédio da Prefeitura Municipal. Em data de 29/01/1983 foi inaugurado o atual prédio da Câmara Municipal, na administração do Prefeito José Fernandes dos Reis.

			O auditório da Câmara, pela Lei nº 323/96, sancionada pelo Prefeito Josias Gomes em 22 de maio de 1996, recebeu a denominação de "Vereador Antonio Gomes de Oliveira", enquanto que o Plenário é denoninado Dr. Euclides Machado de Souza. O prédio foi cedido à Câmara Municipal em "Cessão de Uso" por prazo indeterminado, em 18 de outubro de 2001, pelo então prefeito João Mauro Bernardo.
